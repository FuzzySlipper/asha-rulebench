import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { join } from 'node:path';

const generatedPath = join(process.cwd(), 'libs/protocol/src/generated/api-types.ts');

export function renderApiTypes() {
  return `// GENERATED by the project protocol generator. Do not hand-edit.

export interface RulebenchScenarioReadoutDto {
  readonly id: string;
  readonly title: string;
  readonly summary: string;
  readonly seedLabel: string;
  readonly grid: RulebenchGridDto;
  readonly combatants: readonly RulebenchCombatantDto[];
  readonly selectedAction: RulebenchSelectedActionDto;
  readonly selectedTarget: RulebenchSelectedTargetDto;
  readonly domainEvents: readonly RulebenchDomainEventDto[];
  readonly trace: readonly RulebenchTraceEntryDto[];
  readonly finalState: RulebenchFinalStateDto;
}

export interface RulebenchGridDto {
  readonly width: number;
  readonly height: number;
  readonly cells: readonly RulebenchGridCellDto[];
}

export interface RulebenchGridCellDto {
  readonly x: number;
  readonly y: number;
  readonly terrainTags: readonly string[];
}

export interface RulebenchCombatantDto {
  readonly id: string;
  readonly name: string;
  readonly team: 'ally' | 'enemy';
  readonly position: RulebenchGridPositionDto;
  readonly hitPoints: RulebenchBoundedValueDto;
  readonly defenses: readonly RulebenchNamedNumberDto[];
  readonly conditions: readonly string[];
  readonly isActor: boolean;
}

export interface RulebenchGridPositionDto {
  readonly x: number;
  readonly y: number;
}

export interface RulebenchBoundedValueDto {
  readonly current: number;
  readonly max: number;
}

export interface RulebenchNamedNumberDto {
  readonly id: string;
  readonly label: string;
  readonly value: number;
}

export interface RulebenchSelectedActionDto {
  readonly id: string;
  readonly name: string;
  readonly actorId: string;
  readonly targetIds: readonly string[];
  readonly actionText: string;
  readonly effectText: string;
}

export interface RulebenchSelectedTargetDto {
  readonly targetId: string;
  readonly legality: 'accepted' | 'rejected';
  readonly reason: string;
}

export interface RulebenchDomainEventDto {
  readonly sequence: number;
  readonly type: string;
  readonly summary: string;
  readonly entityIds: readonly string[];
}

export interface RulebenchTraceEntryDto {
  readonly sequence: number;
  readonly phase: 'proposal' | 'validation' | 'resolution' | 'commit';
  readonly status: 'accepted' | 'rejected' | 'info';
  readonly message: string;
  readonly detail: string;
}

export interface RulebenchFinalStateDto {
  readonly summary: string;
  readonly combatants: readonly RulebenchFinalCombatantStateDto[];
}

export interface RulebenchFinalCombatantStateDto {
  readonly id: string;
  readonly name: string;
  readonly hitPoints: RulebenchBoundedValueDto;
  readonly conditions: readonly string[];
}
`;
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const nextContent = renderApiTypes();
  if (process.argv.includes('--check')) {
    const currentContent = readFileSync(generatedPath, 'utf8');
    if (currentContent !== nextContent) {
      console.error(`${generatedPath} is out of date. Run node libs/protocol/scripts/generate-api-types.mjs.`);
      process.exit(1);
    }
  } else {
    writeFileSync(generatedPath, nextContent);
  }
}
