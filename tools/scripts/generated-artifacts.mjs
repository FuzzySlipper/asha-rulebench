import {
  existsSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { join } from "node:path";
import { tmpdir } from "node:os";
import { renderApiTypes } from "../../libs/protocol/scripts/generate-api-types.mjs";
import {
  renderRpgContentArtifact,
  RPG_CONTENT_ARTIFACT_SCHEMA,
} from "./rpg-content-artifact.mjs";

const root = process.cwd();
const writeMode = process.argv.includes("--write");
const artifacts = [
  {
    emitter: "libs/content-authoring",
    path: "rulebench-rs/crates/rulebench-content/src/generated/representative-rpg-content.json",
    provenance: RPG_CONTENT_ARTIFACT_SCHEMA,
    render: renderRpgContentArtifact,
  },
  {
    emitter: "rulebench-codegen/emit_protocol_types",
    path: "libs/protocol/src/generated/api-types.ts",
    render: renderApiTypes,
  },
];

runFocusedFailureTests();

if (writeMode) {
  for (const artifact of artifacts) {
    writeFileSync(join(root, artifact.path), artifact.render());
    console.log(`updated ${artifact.path} from ${artifact.emitter}`);
  }
  process.exit(0);
}

const temporaryRoot = mkdtempSync(join(tmpdir(), "asha-rulebench-generated-"));
const failures = [];
try {
  for (const [index, artifact] of artifacts.entries()) {
    const expected = artifact.render();
    const temporaryPath = join(temporaryRoot, `${index}.ts`);
    writeFileSync(temporaryPath, expected);
    const committedPath = join(root, artifact.path);
    const actual = existsSync(committedPath)
      ? readFileSync(committedPath, "utf8")
      : null;
    failures.push(
      ...validateArtifact(
        artifact,
        readFileSync(temporaryPath, "utf8"),
        actual,
      ),
    );
  }
} finally {
  rmSync(temporaryRoot, { recursive: true, force: true });
}

if (failures.length > 0) {
  console.error(failures.join("\n"));
  process.exit(1);
}

console.log(`generated artifact check ok (${artifacts.length} Rust emitters)`);

function validateArtifact(artifact, expected, actual) {
  if (actual === null) {
    return [
      `${artifact.emitter}: committed artifact is missing: ${artifact.path}. Run pnpm run generated:write.`,
    ];
  }
  const hasProvenance =
    artifact.provenance === undefined
      ? actual.startsWith("// GENERATED by ") && actual.includes("schema ")
      : actual.includes(artifact.provenance);
  if (!hasProvenance) {
    return [
      `${artifact.emitter}: ${artifact.path} lacks generated provenance/schema metadata. Run pnpm run generated:write.`,
    ];
  }
  if (actual !== expected) {
    return [
      `${artifact.emitter}: ${artifact.path} is stale. Run pnpm run generated:write.`,
    ];
  }
  return [];
}

function runFocusedFailureTests() {
  const fixture = { emitter: "fixture/emitter", path: "generated.ts" };
  assertRejected(
    validateArtifact(fixture, "// GENERATED by fixture schema 1\nnew", null),
    "missing artifact",
  );
  assertRejected(
    validateArtifact(
      fixture,
      "// GENERATED by fixture schema 1\nnew",
      "hand written",
    ),
    "unmarked artifact",
  );
  assertRejected(
    validateArtifact(
      fixture,
      "// GENERATED by fixture schema 1\nnew",
      "// GENERATED by fixture schema 1\nold",
    ),
    "stale artifact",
  );
}

function assertRejected(failures, label) {
  if (failures.length === 0) {
    throw new Error(`Generated-artifact self-test failed to reject ${label}.`);
  }
}
