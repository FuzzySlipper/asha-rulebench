import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const root = process.cwd();
const boundaries = JSON.parse(readFileSync(join(root, 'boundaries.json'), 'utf8'));
const depConstraints = [...boundaries.typeConstraints, ...boundaries.scopeConstraints];
const output = `// Generated by tools/scripts/gen-eslint-boundaries.mjs. Do not hand-edit.
import nx from '@nx/eslint-plugin';

const depConstraints = ${JSON.stringify(depConstraints, null, 2)};

export default [
  ...nx.configs['flat/base'],
  ...nx.configs['flat/typescript'],
  ...nx.configs['flat/javascript'],
  {
    ignores: ['dist/**', 'coverage/**', 'node_modules/**', 'tmp/**']
  },
  {
    files: ['**/*.ts', '**/*.tsx', '**/*.js', '**/*.jsx', '**/*.mts', '**/*.mjs'],
    rules: {
      '@nx/enforce-module-boundaries': [
        'error',
        {
          allow: ['^.*/eslint(\\\\.base)?\\\\.config\\\\.[cm]?[jt]s$'],
          depConstraints,
          enforceBuildableLibDependency: false
        }
      ],
      '@typescript-eslint/consistent-type-imports': 'error',
      '@typescript-eslint/no-explicit-any': 'error',
      '@typescript-eslint/no-non-null-assertion': 'error'
    }
  }
];
`;

const path = join(root, 'eslint.config.mjs');
if (process.argv.includes('--check')) {
  const current = readFileSync(path, 'utf8');
  if (current !== output) {
    console.error('eslint.config.mjs is stale. Run `node tools/scripts/gen-eslint-boundaries.mjs`.');
    process.exit(1);
  }
  console.log('eslint boundary config ok');
} else {
  writeFileSync(path, output);
}
