fn render_combat_session_catalog() -> String {
    let transcripts = combat_session_transcripts();
    let mut out = String::from(
        "// GENERATED by rulebench-authority Rust combat session transcript. Do not hand-edit.\n\n",
    );
    out.push_str(
        "import type { RulebenchCombatSessionCatalogDto } from '@asha-rulebench/protocol';\n\n",
    );
    out.push_str(
        "export const rustBackedCombatSessionCatalog: RulebenchCombatSessionCatalogDto = {\n",
    );
    out.push_str("  summaries: [\n");
    for transcript in &transcripts {
        out.push_str(&render_session_summary(&transcript.summary));
    }
    out.push_str("  ],\n");
    out.push_str("  readouts: [\n");
    for transcript in &transcripts {
        for readout in &transcript.steps {
            out.push_str(&render_step_readout(readout));
        }
    }
    out.push_str("  ],\n");
    out.push_str("  controlHistoryReadouts: [\n");
    for readout in combat_session_control_history_readouts() {
        out.push_str(&render_control_history_readout(&readout));
    }
    out.push_str("  ],\n");
    out.push_str("  scriptReadouts: [\n");
    for readout in combat_session_script_readouts() {
        out.push_str(&render_script_readout(&readout));
    }
    out.push_str("  ],\n");
    out.push_str("  automaticRunReadouts: [\n");
    for readout in combat_session_automatic_run_readouts() {
        out.push_str(&render_automatic_run_readout(&readout));
    }
    out.push_str("  ],\n");
    out.push_str("};\n");
    out
}

fn render_session_summary(summary: &CombatSessionSummary) -> String {
    let mut out = String::from("    {\n");
    out.push_str(&format!("      id: {},\n", ts_string(&summary.id)));
    out.push_str(&format!("      title: {},\n", ts_string(&summary.title)));
    out.push_str(&format!(
        "      summary: {},\n",
        ts_string(&summary.summary)
    ));
    out.push_str(&format!(
        "      seedLabel: {},\n",
        ts_string(&summary.seed_label)
    ));
    out.push_str("      steps: [\n");
    for step in &summary.steps {
        out.push_str(&render_step_summary(step, "        "));
    }
    out.push_str("      ],\n");
    out.push_str("    },\n");
    out
}

fn render_step_readout(readout: &CombatSessionStepReadout) -> String {
    let mut scenario = readout.scenario.clone();
    scenario.metadata = ScenarioMetadata {
        id: readout.step.id.clone(),
        title: readout.step.title.clone(),
        summary: readout.step.summary.clone(),
        seed_label: format!(
            "roll-stream:{}",
            readout
                .command
                .roll_stream
                .iter()
                .map(i32::to_string)
                .collect::<Vec<_>>()
                .join(",")
        ),
    };
    let target = readout
        .receipt
        .target_legality
        .as_ref()
        .expect("session readout has target legality");

    let mut out = String::from("    {\n");
    out.push_str(&format!(
        "      sessionId: {},\n",
        ts_string(&readout.session_id)
    ));
    out.push_str("      step: ");
    out.push_str(&render_step_summary(&readout.step, "      "));
    out.push_str("      command: ");
    out.push_str(&render_command(&readout.command, "      "));
    out.push_str("      scenarioReadout: ");
    out.push_str(&render_scenario_readout(
        &scenario,
        &readout.receipt.events,
        &readout.receipt.trace,
        &readout.state_after,
        target,
        "      ",
    ));
    out.push_str(",\n");
    out.push_str("      combatLog: [\n");
    for entry in &readout.combat_log {
        out.push_str(&render_log_entry(entry, "        "));
    }
    out.push_str("      ],\n");
    out.push_str("      actionResourceLedger: ");
    out.push_str(&render_action_resource_ledger(
        &readout.action_resource_ledger,
        "      ",
    ));
    out.push_str(",\n");
    out.push_str("      stateBefore: ");
    out.push_str(&render_state(&readout.state_before, "      "));
    out.push_str(",\n");
    out.push_str("      stateAfter: ");
    out.push_str(&render_state(&readout.state_after, "      "));
    out.push_str(",\n");
    out.push_str("    },\n");
    out
}

fn render_action_resource_ledger(ledger: &ActionResourceLedgerReadout, indent: &str) -> String {
    let mut out = String::from("{\n");
    out.push_str(&format!("{indent}  combatants: [\n"));
    for combatant in &ledger.combatants {
        out.push_str(&format!("{indent}    {{\n"));
        out.push_str(&format!(
            "{indent}      combatantId: {},\n",
            ts_string(&combatant.combatant_id)
        ));
        out.push_str(&format!("{indent}      resources: [\n"));
        for resource in &combatant.resources {
            out.push_str(&render_action_resource_state(resource, indent));
        }
        out.push_str(&format!("{indent}      ],\n"));
        out.push_str(&format!("{indent}    }},\n"));
    }
    out.push_str(&format!("{indent}  ],\n"));
    out.push_str(&format!("{indent}}}"));
    out
}

