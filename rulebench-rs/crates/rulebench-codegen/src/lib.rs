//! Rulebench-local protocol and artifact generation support.
//!
//! This reserved boundary will own TypeScript protocol emission and checked
//! scenario/session artifact rendering. It is not portable rule authority.

#![forbid(unsafe_code)]

mod combat_session {
    pub(crate) mod fragments;
    pub(crate) mod renderers;
    pub(crate) mod scalars;
}
mod scenario_catalog;
mod ts_emit;

pub use scenario_catalog::render_scenario_catalog;

pub fn render_combat_session_catalog() -> String {
    use combat_session::renderers::{
        render_automatic_run_readout, render_automatic_run_replay_readout,
        render_control_history_readout, render_script_readout, render_session_summary,
        render_step_readout,
    };

    let transcripts = rulebench_fixtures::aggregated_combat_session_transcripts();
    let mut out = String::from(
        "// GENERATED by rulebench-authority Rust combat session transcript. Do not hand-edit.\n\n",
    );
    out.push_str(
        "import type { RulebenchCombatSessionCatalogDto } from '@asha-rulebench/protocol';\n\n",
    );
    out.push_str("export const rustBackedCombatSessionCatalog: RulebenchCombatSessionCatalogDto = {\n  summaries: [\n");
    for transcript in &transcripts {
        out.push_str(&render_session_summary(&transcript.summary));
    }
    out.push_str("  ],\n  readouts: [\n");
    for transcript in &transcripts {
        for readout in &transcript.steps {
            out.push_str(&render_step_readout(readout));
        }
    }
    out.push_str("  ],\n  controlHistoryReadouts: [\n");
    for readout in rulebench_fixtures::aggregated_combat_session_control_history_readouts() {
        out.push_str(&render_control_history_readout(&readout));
    }
    out.push_str("  ],\n  scriptReadouts: [\n");
    for readout in rulebench_fixtures::aggregated_combat_session_script_readouts() {
        out.push_str(&render_script_readout(&readout));
    }
    out.push_str("  ],\n  automaticRunReadouts: [\n");
    for readout in rulebench_fixtures::aggregated_combat_session_automatic_run_readouts() {
        out.push_str(&render_automatic_run_readout(&readout));
    }
    out.push_str("  ],\n  automaticRunReplayReadouts: [\n");
    for readout in rulebench_fixtures::aggregated_combat_session_automatic_run_replay_readouts() {
        out.push_str(&render_automatic_run_replay_readout(&readout));
    }
    out.push_str("  ],\n};\n");
    out
}

#[cfg(test)]
mod tests {
    use super::{render_combat_session_catalog, render_scenario_catalog};

    const GENERATED_SCENARIO_CATALOG: &str =
        include_str!("../../../../libs/transport/src/generated/rust-scenario-catalog.ts");
    const GENERATED_COMBAT_SESSION: &str =
        include_str!("../../../../libs/transport/src/generated/rust-combat-session.ts");

    #[test]
    fn scenario_catalog_renderer_matches_the_committed_artifact() {
        assert_eq!(render_scenario_catalog(), GENERATED_SCENARIO_CATALOG);
    }

    #[test]
    fn combat_session_renderer_matches_the_committed_artifact() {
        assert_eq!(render_combat_session_catalog(), GENERATED_COMBAT_SESSION);
    }
}
