mod aliases;
mod interfaces;
mod live_interfaces;

pub use aliases::aliases;
pub use interfaces::interfaces;
pub use live_interfaces::live_interfaces;

/// One field in a stable TypeScript interface contract.
pub struct ProtocolField {
    pub name: &'static str,
    pub ty: &'static str,
}

/// One stable TypeScript interface contract.
pub struct ProtocolInterface {
    pub name: &'static str,
    pub fields: &'static [ProtocolField],
}

/// One stable TypeScript alias contract.
pub type ProtocolAlias = &'static str;

/// Renders the checked TypeScript protocol from Rust-owned metadata.
pub fn render_api_types() -> String {
    let mut out =
        String::from("// GENERATED by rulebench-protocol Rust metadata. Do not hand-edit.\n\n");
    for alias in aliases() {
        out.push_str(alias);
        out.push_str("\n\n");
    }
    for interface in live_interfaces().iter().chain(interfaces()) {
        out.push_str(&format!("export interface {} {{\n", interface.name));
        for field in interface.fields {
            out.push_str(&format!("  readonly {}: {};\n", field.name, field.ty));
        }
        out.push_str("}\n\n");
    }
    if out.ends_with("\n\n") {
        out.pop();
    }
    out
}
